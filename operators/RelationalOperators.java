public class RelationalOperators {
    public static void main(String[] args) {
        // Relational operators:-  ==, !=, >, >=, <=

        int a = 10;
        int b = 10;
        System.out.println(a==b); // check equal values

        System.out.println(a!=b); // check not equal value  it is negation of equal

        int x= 10;
        int y = 5;

        System.out.println(x>y); //check greater 
        System.out.println(x<y); //check lesser

        System.out.println(x>=y); // it check  greater or equal
        System.out.println(x<=y); // it check  lesser or equal
        System.out.println(y<=x); // it check  lesser or equal


    }
}
